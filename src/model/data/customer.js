class User {
    my_id;
    my_name;
    my_addr;
    my_age;
    my_birth;
    my_sex;

    get id() {
        return this.my_id;
    }

    set id(value) {
        this.my_id = value;
    }

    get name() {
        return this.my_name;
    }

    set name(value) {
        this.my_name = value;
    }

    get addr() {
        return this.my_addr;
    }

    set addr(value) {
        this.my_addr = value;
    }

    get age() {
        return this.my_age;
    }

    set age(value) {
        this.my_age = value;
    }

    get birth() {
        return this.my_birth;
    }

    set birth(value) {
        this.my_birth = value;
    }

    get sex() {
        return this.my_sex;
    }

    set sex(value) {
        this.my_sex = value;
    }
}
export {User};
